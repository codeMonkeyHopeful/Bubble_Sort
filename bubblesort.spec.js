describe('Bubble Sort', function(){
    it('handles an empty array', function(){
      expect( bubbleSort([]) ).toEqual( [] );
    });
  });

describe('Bubble Sort', function(){
  it('handles an empty array', function(){
    expect( bubbleSort([3,1,2]) ).toEqual( [1,2,3] );
  });
});

describe('Bubble Sort', function(){
  it('handles an empty array', function(){
    expect( bubbleSort([3,1,2,4]) ).toEqual( [1,2,3,4] );
  });
});
